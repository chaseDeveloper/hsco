		Map inMap = null;
		if (inDsMap != null) {
			inMap = inDsMap.get(0);
		}

		List<Map> records = (List<Map>) baseDao.list("HRM060201DAO.selectTrgetEmpl", inMap);

		DataSetMap outDsMap = new DataSetMap();
